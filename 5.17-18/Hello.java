public class Hello { // Hello클래스
    public static void main(String[] args) { //메인 메소드(method)-프로그램 시작점
        //문자열(string)은 큰따옴표로 감싸야함
        System.out.println("Hello world");//동작하게 만드는 것=문장(statement) 문장은 ';'으로 마무리
        System.out.println("12345");
        System.out.println("010-0000-0000"); //문자열 출력
    }
}

// 메소드 : 프로그램이 해야 하는 작업을 적어놓은 곳
// 메소드 안의 문장을 위에서부터 차례대로 실행