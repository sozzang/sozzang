public class Plus{
    public static void main(String[] args){
 
        System.out.println(10 + "10");
        //정수 + "문자열" 은 더하기 연산이 아니라 그냥 나열될 뿐
        
        System.out.println("20" + "10");
        //"문자열" + "문자열"도 그냥 나열
        System.out.println();
        //빈 줄 출력
        System.out.println(5.125);
        System.out.println(0.1 + 0.1);

        System.out.print(1);
        System.out.print(2);
        System.out.print(3); //ln이 없으면 같은 줄에 123이 나란히 출력 
        }
}