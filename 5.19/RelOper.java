//관계연산자 
//결과값은 true, false로 계산된다

public class RelOper {
	public static void main(String[] args){
		System.out.println(15 == 55);
		//15와 55가 같나요?
		System.out.println(15 != 55);
		//15와 55가 다른가요?
		System.out.println(15 > 55);
		//15가 55보다 크나요?
		System.out.println(15 >= 55);
		//15가 55보다 크거나 같나요?
		System.out.println(15 < 55);
		//15가 55보다 작나요?
		System.out.println(15 <= 55);
		//15가 55보다 작거나 같나요?
		
		//'>="와 '<='의 경우에는 모양을 유의
	}
}