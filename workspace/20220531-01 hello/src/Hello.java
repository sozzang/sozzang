//이클립스 첫사용

public class Hello {

	public static void main(String[] args) {

		int number = 10;
		System.out.println("Hello world");
		System.out.println(number);
		number++;
		System.out.println("Hello world");
		System.out.println(number);
		number++;
		System.out.println("Hello world");
		System.out.println(number);

		for (int i = 0; i < 10; i++) {
			System.out.println(i);
		}

	}
}